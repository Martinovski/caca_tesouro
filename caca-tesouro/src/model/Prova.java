package model;

public class Prova {

	private int id;
	
	private int pontoProva;
	
	private int pontExtra;
	private int tempoProva;
	

	public int getTemoProva() {
		return tempoProva;
	}

	public void setTemoProva(int temoProva) {
		this.tempoProva = temoProva;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getPontoProva() {
		return pontoProva;
	}

	public void setPontoProva(int pontoProva) {
		this.pontoProva = pontoProva;
	}

	public int getPontExtra() {
		return pontExtra;
	}

	public void setPontExtra(int pontExtra) {
		this.pontExtra = pontExtra;
	}
	

}
