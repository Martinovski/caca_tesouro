package model;

public class GerenteCodigo {

	private int id;
	private String dica;
	private int qrCode;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getDica() {
		return dica;
	}

	public void setDica(String dica) {
		this.dica = dica;
	}

	public int getQrCode() {
		return qrCode;
	}

	public void setQrCode(int qrCode) {
		this.qrCode = qrCode;
	}

}
